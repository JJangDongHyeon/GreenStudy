public class FlowEx12 {
    public static void main(String[] args){
        for(int i = 1 ; i < 6 ; i++){
            System.out.print(i +" ");
        }
        System.out.println();
        for(int i = 2 ; i < 6 ; i++){
            System.out.println(i);
        }
    }
}
